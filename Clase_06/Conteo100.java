package Clase_06;

import java.util.Scanner;

public class Conteo100 {
    public static void main(String[] arg) {
        System.out.println("El siguiente programa muestra los numeros del 1 al 100");
        int contador = 0;
        while (contador < 100) {
            contador++;
            System.out.println("contador: " + contador);
        }
    }
}
